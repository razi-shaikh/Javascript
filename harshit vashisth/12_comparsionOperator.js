// == vs ===

let num1="7",num2=7;
// == its check only value not a data type
// num1 is string and num2 is number both are equal
console.log(`== value ${num1==num2}`);
// console.log("== value"+num1==num2);

// === its check value and data type
// num1 is string and num2 number both data type are different and answer is not equal
console.log(`=== value ${num1===num2}`);
// console.log("=== value"+num1===num2);

// != vs !==
// !=
console.log(`!= value ${num1!=num2}`);
// console.log("!= value"+num1 != num2);